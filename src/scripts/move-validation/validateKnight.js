export default function (x1, y1, x2, y2) {
	if (x2 != 0 && y2 != 0) {
		if (
			(y1 + 2 === y2 && x1 + 1 === x2) ||
			(y1 + 2 === y2 && x1 - 1 === x2) ||
			(y1 - 2 === y2 && x1 + 1 === x2) ||
			(y1 - 2 === y2 && x1 - 1 === x2) ||
			(y1 + 1 === y2 && x1 + 2 === x2) ||
			(y1 + 1 === y2 && x1 - 2 === x2) ||
			(y1 - 1 === y2 && x1 + 2 === x2) ||
			(y1 - 1 === y2 && x1 - 2 === x2)
		) {
			return true;
		}
	}
}
